"""
HierarchicalDeepfakeDataset for CRAFT-DF.

Loads face crops and DWT features from the metadata CSV produced by
VideoProcessor, returning (spatial_tensor, frequency_tensor, label) tuples.
"""

import logging
from pathlib import Path
from typing import Optional, Callable, Tuple

import numpy as np
import pandas as pd
import torch
from torch.utils.data import Dataset

logger = logging.getLogger(__name__)


class HierarchicalDeepfakeDataset(Dataset):
    """
    Dataset that reads face crops (.npy) and DWT features (.npy) using a
    metadata CSV generated by VideoProcessor.

    Expected CSV columns (produced by VideoProcessor):
        face_path       - relative path to face crop .npy file
        dwt_path        - relative path to DWT feature .npy file
        label_numeric   - 0 (real) or 1 (fake)

    Args:
        metadata_path:   Path to the metadata CSV file.
        data_root:       Root directory that face_path / dwt_path are relative
                         to.  Defaults to the CSV's parent directory.
        transform:       Optional callable applied to (spatial, freq, label).
        validate_files:  If True, drop rows whose .npy files are missing.
        cache_size:      Number of samples to keep in memory (0 = no cache).
        memory_limit_gb: Soft memory limit for the cache (unused currently,
                         kept for API compatibility).
    """

    def __init__(
        self,
        metadata_path: str,
        data_root: Optional[str] = None,
        transform: Optional[Callable] = None,
        validate_files: bool = True,
        cache_size: int = 1000,
        memory_limit_gb: float = 4.0,
    ):
        self.metadata_path = Path(metadata_path)
        self.data_root = Path(data_root) if data_root else self.metadata_path.parent
        self.transform = transform
        self.cache_size = cache_size
        self._cache: dict = {}

        if not self.metadata_path.exists():
            raise FileNotFoundError(f"Metadata CSV not found: {self.metadata_path}")

        self.metadata = pd.read_csv(self.metadata_path)
        self._validate_columns()

        if validate_files:
            self.metadata = self._drop_missing(self.metadata)

        self.metadata = self.metadata.reset_index(drop=True)
        logger.info(
            f"Dataset loaded: {len(self.metadata)} samples "
            f"({(self.metadata['label_numeric'] == 0).sum()} real, "
            f"{(self.metadata['label_numeric'] == 1).sum()} fake)"
        )

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _validate_columns(self) -> None:
        required = {"face_path", "dwt_path", "label_numeric"}
        missing = required - set(self.metadata.columns)
        if missing:
            raise ValueError(f"Metadata CSV is missing columns: {missing}")

    def _drop_missing(self, df: pd.DataFrame) -> pd.DataFrame:
        """Remove rows whose .npy files do not exist on disk."""
        def both_exist(row):
            return (
                (self.data_root / row["face_path"]).exists()
                and (self.data_root / row["dwt_path"]).exists()
            )

        mask = df.apply(both_exist, axis=1)
        dropped = (~mask).sum()
        if dropped:
            logger.warning(f"Dropped {dropped} rows with missing .npy files")
        return df[mask]

    def _load_sample(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor, int]:
        row = self.metadata.iloc[idx]

        face_arr = np.load(self.data_root / row["face_path"])   # (H, W, C) uint8
        dwt_arr  = np.load(self.data_root / row["dwt_path"])    # flat or (C, H, W)

        # Spatial: uint8 HWC -> float32 CHW in [0, 1]
        spatial = torch.from_numpy(face_arr).float() / 255.0
        if spatial.dim() == 3 and spatial.shape[-1] in (1, 3):
            spatial = spatial.permute(2, 0, 1)          # CHW

        # Frequency: cast to float32, keep shape as-is
        freq = torch.from_numpy(dwt_arr.astype(np.float32))

        label = int(row["label_numeric"])
        return spatial, freq, label

    # ------------------------------------------------------------------
    # Dataset interface
    # ------------------------------------------------------------------

    def __len__(self) -> int:
        return len(self.metadata)

    def __getitem__(self, idx: int):
        if idx in self._cache:
            spatial, freq, label = self._cache[idx]
        else:
            spatial, freq, label = self._load_sample(idx)
            if self.cache_size > 0 and len(self._cache) < self.cache_size:
                self._cache[idx] = (spatial, freq, label)

        sample = (spatial, freq, label)
        if self.transform is not None:
            sample = self.transform(sample)

        return sample

    # ------------------------------------------------------------------
    # Utility methods expected by TrainingPipeline
    # ------------------------------------------------------------------

    def get_class_weights(self) -> torch.Tensor:
        """Return inverse-frequency weights for [real, fake]."""
        counts = self.metadata["label_numeric"].value_counts().sort_index()
        total  = len(self.metadata)
        weights = torch.tensor(
            [total / (2.0 * counts.get(i, 1)) for i in range(2)],
            dtype=torch.float32,
        )
        return weights

    def get_performance_stats(self) -> dict:
        return {
            "total_samples": len(self.metadata),
            "cache_size":    len(self._cache),
            "real_samples":  int((self.metadata["label_numeric"] == 0).sum()),
            "fake_samples":  int((self.metadata["label_numeric"] == 1).sum()),
        }
